const month = 3;
switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    default:
        console.log("default case match")
        break;
}
// break keyword breaks the condition where it matches otherwise all the coditions after the matched condition will be executed except default  